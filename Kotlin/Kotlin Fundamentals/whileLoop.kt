package com.mimo.kotlinpractice
// PRO0GRAM TO PRINT HELLO WORLD 10 TIMES USING WHILE LOOP
fun main()
{
    var num = 1
    while(num<=10)
    {
        println("HELLO")
        num++
    }
}

