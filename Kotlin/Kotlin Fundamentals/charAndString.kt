package com.mimo.kotlinpractice

fun main()
{

    // Char
    val myChar:Char = 'M'        // Character always kept inside single inverted commas
    print("My Character: ")
    print(myChar)
    print("\n \n")

    // String
    val myString = "Hello are you enjoying the free icoders android classes by Mimo"
    // Strings are always kept inside double inverted commas
    print("My String: ")
    print(myString)
    print("\n \n")


    // Storing single Character in String

    val charToString = "a"      // string can store both a single character and a line of paragraph
    print("Changing Char to String: ")   // but it has to inside double inverted commas
    print(charToString)


}