package com.mimo.kotlinpractice

fun main()
{
    val num =10
    do{
        println("Check How this statement is printing even after the while condition doesn't met")
    }while (num>12)
}

/*
    ** DO While Notes
    * do while will execute one time at least even if the condition is wrong
    * keep in mind to provide a end case in while and do while loop else the loop may run for
      infinite times
 */